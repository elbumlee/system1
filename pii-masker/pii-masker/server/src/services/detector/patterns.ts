// --- 1. 주민등록번호 ---
export const RRN_PATTERNS = {
  // 하이픈 포함: 123456-1234567
  withHyphen: /\d{6}-[1-4]\d{6}/g,
  // 부분 마스킹 원본: 123456-1****** 등
  partialMasked: /\d{6}-[1-4]\d{0,6}[*]{1,6}/g,
  // 하이픈 없이 13자리 (문맥 키워드 필요)
  noHyphen: /\d{13}/g,
};

// 주민번호 문맥 키워드
